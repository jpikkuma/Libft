#include "libft.h"

char	*ft_strcpy(char *s1, const char *s2)
{
	char	*tmp;

	if (s1 == s2)
		return (s1);
	tmp = s1;
	while (*s2)
		*tmp++ = *s2++;
	*tmp = '\0';
	return (s1);
}
